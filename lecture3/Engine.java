package FLS.lesson3;

public class Engine {
    double volume;
    double power;

    public Engine() {
        this.volume = 0;
        this.power = 0;
    }

    public double getVolume() {
        return volume;
    }

    public void setVolume(double volume) {
        this.volume = volume;
    }

    public double getPower() {
        return power;
    }

    public void setPower(double power) {
        this.power = power;
    }

}
